            <h1 class="projects" style="margin-bottom: 30px;">The better the briefing, the better the project. <span class="project-title">Briefings</span></h1>
            <p class="top">When I started managing some projects at Cintra, I realized the importance of gathering detailed information about any work before starting it.</p>
            <p>But it is very rare for the client to be able to transmit exactly what they want. We received documents that left more questions than they directed.</p>
            <p>I decided that we needed to create a new process.</p>
            <h2 style="margin-top: 4rem;">Engineering Process:</h2>
            <ul class="processes">
                <li class="top">Understand the problem</li>
                <li>Imagine solutions</li>
                <li>Plan</li>
                <li>Prototype</li>
                <li>Test</li>
                <li>Improve</li>
            </ul>
            <h1 class="projects" style="font-size: 50px; margin-top: 6rem; margin-bottom: 1.7rem;">Understanding the problem</h1>
            <p class="top">After analyzing why our briefings were not so effective, I spoke with other agencies to see if they had the same problems...</p>
            <p class="top">Most said they prefer to have a meeting with the client after sending the briefing, but they said that this usually delays the work because the meetings take a lot of production time.</p>
            <h1 class="projects" style="font-size: 50px; margin-top: 5rem; margin-bottom: 1.7rem;">Imagining solutions</h1>
            <p class="top">Discussing with a partner of the agency, we defined a model that could solve most of these problems. The idea was to create a briefing as a wizard questionnaire for the browser. Any agency could access, send to the client and receive the answer by email in sequence.</p>
            <h1 class="projects" style="font-size: 50px; margin-top: 5rem; margin-bottom: 1.7rem;">Planning</h1>
