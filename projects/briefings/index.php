            <h1 class="projects" style="font-size: 50px; margin-top: 5rem; margin-bottom: 1.7rem;">Testing</h1>
            <p class="top">We tested with some clients and it worked, but it was still not better than before. The clients still had difficulty with some questions and did not answer or called to ask.</p>
            <p class="top">We solved this by creating an explanation box in the questions that presented more doubts.</p>
            <img src="../../assets/briefings-prototype-7.jpg" style="width: inherit; margin-top: 1rem;" />
            <h1 class="projects" style="font-size: 50px; margin-top: 5rem; margin-bottom: 1.7rem;">Improving</h1>
            <p class="top">We made some improvements at the end of the questionnaire. After the last question, the answers were already sent without a final review step. After creating the review step, the quality of the answers improved a lot.</p>
            <p class="top" style="margin-bottom: 5rem;">We also realized that there is no perfect briefing, it is always necessary to have a quick meeting with the clients to align the project, etc.</p>
