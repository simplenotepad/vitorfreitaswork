            <h1 class="projects" style="margin-bottom: 30px;">Why create a new social network? <span class="project-title">Norun</span></h1>
            <p class="top">It is highly likely that the business model of social networks is not sustainable in the long term. The competition for users' attention slowly undermines the entire structure of our society, from physical/mental to social/professional aspects.</p>
            <p>Norun's challenge is to understand the best way to structure a new model of social network. This is one of the most difficult problems I have worked on, but it is very important.</p>
            <h2 style="margin-top: 4rem;">Engineering Process:</h2>
