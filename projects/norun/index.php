            <h1 class="projects" style="font-size: 50px; margin-top: 6rem; margin-bottom: 1.7rem;">Define the problem</h1>
            <p class="top">In this phase, I focused on closely observing how social networks fit into people's lives. I noticed that when trying to start a conversation, people quickly become defensive and do not share their thoughts on the subject.</p>
            <p class="top">The main problems defined were the unlimited amount of content, monetization model, irrelevant push notifications, and comparison metrics between users.</p>
